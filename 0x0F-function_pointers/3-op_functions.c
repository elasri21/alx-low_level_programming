#include "3-calc.h"
#include <stdio.h>
#include <stdlib.h>
#include <string.h>
/**
 * op_add - add function
 * @a: first integer
 * @b: second integer
 * Return: integer
 */
int op_add(int a, int b)
{
return (a + b);
}
/**
 * op_sub - sub function
 * @a: first integer
 * @b: second integer
 * Return: integer
 */
int op_sub(int a, int b)
{
return (a - b);
}
/**
 * op_mul - multiplacation func
 * @a: first integer
 * @b: second integer
 * Return: integer
 */
int op_mul(int a, int b)
{
return (a * b);
}
/**
 * op_div - div func
 * @a: first integer
 * @b: second integer
 * Return: integer
 */
int op_div(int a, int b)
{
return (a / b);
}
/**
 * op_mod - modulo func
 * @a: first integer
 * @b: second integer
 * Return: integer
 */
int op_mod(int a, int b)
{
return (a % b);
}
